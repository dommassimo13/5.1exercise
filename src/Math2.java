public class Math2 {
    public static void main(String [] args) {
        System.out.println("Math.pow(7, 9) returns " + Math.pow(7, 9));
        System.out.println("Math.sqrt(36) returns " + Math.sqrt(36));
        System.out.println("Math.abs(-9) returns " + Math.abs(-9));
        System.out.println("Math.random() returns " + Math.random());
        System.out.println("Math.max(4, 6) returns " + Math.max(4, 6));
        System.out.println("Math.min(1, 5) returns " + Math.min(1, 5));
        System.out.println("Math.round(5.72) returns " + Math.round(5.72));
        System.out.println("Math.ceil(8.37) returns " + Math.ceil(8.37));
        System.out.println("Math.floor(4.75) returns " + Math.floor(4.75));
    }
}
